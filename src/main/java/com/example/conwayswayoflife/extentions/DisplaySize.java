package com.example.conwayswayoflife.extentions;

public class DisplaySize {

    public DisplaySize(double x1, double y1){
        x = x1;
        y = y1;
    }

    private double x = 0;
    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }

    private double y = 0;
    public double getY() {
        return y;
    }
    public void setY(double y) {
        this.y = y;
    }
}
